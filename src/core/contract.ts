/** One batter in a batting order, or one player in a soccer XI. */
export interface LineupSpot {
  /** 1-based order (batting slot, or shirt position index). */
  order: number;
  /** Full display name; never empty. */
  name: string;
  /** Position code, e.g. 'SS', 'C', 'LF' (baseball) / 'G', 'LB' (soccer). '' if unknown. */
  position: string;
  /** Jersey/shirt number as text, or undefined. */
  jersey?: string;
}

export interface BaseballState {
  kind: 'baseball';
  balls: number; // 0–4 defensively clamped
  strikes: number; // 0–3
  outs: number; // 0–3
  /** Runner display name on each base, or undefined when empty. */
  bases: { first?: string; second?: string; third?: string };
  atBat?: string;
  onDeck?: string;
  pitcher?: string;
  /** Batting orders; either may be empty when not yet posted. */
  lineups: { home: LineupSpot[]; away: LineupSpot[] };
}

export interface SoccerState {
  kind: 'soccer';
  home: { formation?: string; starters: LineupSpot[]; bench: LineupSpot[] };
  away: { formation?: string; starters: LineupSpot[]; bench: LineupSpot[] };
}

/** One drafted champion in a LoL composition. */
export interface DraftPick {
  /** 'top' | 'jungle' | 'mid' | 'bottom' | 'support' (lowercased); '' if unknown. */
  role: string;
  /** Champion name as the feed gives it, e.g. 'Vayne', 'JarvanIV'. Never empty. */
  champion: string;
  /** Player/summoner display name; '' if unknown. */
  player: string;
}

export interface EsportsState {
  kind: 'esports';
  /** Game patch, e.g. '16.13'; undefined if unknown. */
  patch?: string;
  blue: { teamCode: string; picks: DraftPick[] };
  red: { teamCode: string; picks: DraftPick[] };
  /** Live totals from the latest frame, when non-zero/known. */
  gold?: { blue: number; red: number };
  kills?: { blue: number; red: number };
}

export type GameState = BaseballState | SoccerState | EsportsState;

export interface SportProvider {
  /** 'espn' | 'mlb' | 'nhl' | 'naver' | 'lolesports' | 'pandascore'. */
  readonly id: string;
  readonly displayName: string;
  /** True ⇒ provider is hidden until its secret exists (see CONTRACT §PandaScore). */
  readonly requiresSecret: string | undefined;
  /** Static or cheap; called at activation and on manual refresh. */
  listLeagues(ctx: ProviderContext): Promise<League[]>;
  /** Current scoreboard window for one league (provider-defined "today"). */
  listGames(ctx: ProviderContext, league: League): Promise<Game[]>;
  /**
   * Full snapshot for one game. May populate PlaySnapshot.state with live
   * "current state" (bases/count, lineups, draft) when the provider and sport
   * support it and gameState is enabled — see CONTRACT §11. Providers without
   * state leave it undefined.
   */
  fetchPlays(ctx: ProviderContext, game: Game): Promise<PlaySnapshot>;
}

// ---------------------------------------------------------------------------
// Relay engine (pure — no IO, no timers)
// ---------------------------------------------------------------------------

export interface RelayEmission {
  /** Events to append to the relay, already ordered for display. */
  events: PlayEvent[];
  /** Game snapshot after this ingest (drives status bar / tree). */
  game: Game;
  /** True when home/away score differs from the previous ingest. */
  scoreChanged: boolean;
  /**
   * Set when phase transitioned this ingest (e.g. 'in' → 'post').
   * Pinned: undefined on the first-ever ingest — the final-score system line
   * fires on the FIRST ingest whose phase is 'post' (see CONTRACT §3.8), not
   * on transitions.
   */
  phaseTransition: { from: GamePhase; to: GamePhase } | undefined;
  /**
   * Passed through UNCHANGED from the ingested PlaySnapshot.state. The engine
   * never diffs or emits it — the UI stashes it on the followed entry and the
   * tree renders it (CONTRACT §11). undefined when the snapshot had no state.
   */
  state?: GameState;
}

export interface RelayEngineOptions {
  /** Max events emitted on first ingest of a game (older ones summarized). */
  backfillLimit: number;
  /** Locale for engine-generated system lines ('en' | 'ko'). */
  locale: RelayLocale;
}

export interface RelayEngine {
  /** Idempotent per snapshot; returns only never-before-emitted lines. */
  ingest(snapshot: PlaySnapshot): RelayEmission;
}

export type RelayLocale = 'en' | 'ko';

/** CONTRACT §12: how granular the relay's event stream is. */
export type DetailLevel = 'summary' | 'detailed';

// ---------------------------------------------------------------------------
// Poll scheduler (owns timers; injectable clock for tests)
// ---------------------------------------------------------------------------

export interface PollPolicy {
  /** Seconds between polls while phase === 'in'. Clamped 10–120, default 20. */
  liveSeconds: number;
  /** Seconds between polls while phase === 'pre'. Fixed 60 (20 within 2 min of start). */
  preSeconds: number;
  /** Global max concurrent in-flight fetches across all games. Fixed 4. */
  maxConcurrent: number;
  /** Backoff: min(interval · 2^failures, 300s); rate-limit ⇒ max(retryAfterMs, 60s). */
  maxBackoffSeconds: number;
}

/**
 * Persisted in workspaceState. Array order IS follow order (append on follow,
 * remove on unfollow, re-follow moves the entry to the end). No cross-reload
 * relay dedup state is persisted: a restored follow gets a FRESH RelayEngine
 * and its first ingest is governed by the backfill cap.
 */
export interface FollowedGameState {
  gameId: string;
  providerId: string;
  leagueId: string;
  /** Epoch ms of the follow action. */
  followedAt: number;
  /** Epoch ms when the game was first observed phase==='post' (0 = not yet). */
  postObservedAt: number;
  /** Last-known display snapshot; renders tree/status bar after reload before first fetch. */
  lastKnown: {
    awayAbbrev: string;
    homeAbbrev: string;
    awayScore: number | undefined;
    homeScore: number | undefined;
    statusShort: string;
    phase: GamePhase;
  };
}

// ---------------------------------------------------------------------------
// Module seams — implemented by the files named in CONTRACT.md §10. These
// interfaces exist so four tasks can build in parallel and link on first try.
// ---------------------------------------------------------------------------

export interface SchedulerTimers {
  setTimeout(fn: () => void, ms: number): unknown;
  clearTimeout(handle: unknown): void;
  now(): number;
}

/** Global fetch-concurrency gate (CONTRACT §4: max 4). */
export interface Semaphore {
  /** Resolves with a release function; callers MUST release in finally. */
  acquire(): Promise<() => void>;
}

export interface GamePollerCallbacks {
  /** Bound closure over (provider, ctx, game). Throws ProviderError only. */
  fetchSnapshot(): Promise<PlaySnapshot>;
  /** Every ingest result (even with zero events — status bar reads game). */
  onEmission(emission: RelayEmission): void;
  /** Poller-generated system lines, as i18n keys (§5) — UI translates+appends. */
  onSystemLine(key: string, params?: Record<string, string | number>): void;
  /** Fired by the post+10min one-shot, or 3× not-found. UI removes the follow. */
  onAutoUnfollow(): void;
  /** Diagnostics channel sink (stacks, payload heads). */
  onDiagnostic(message: string): void;
}

export interface GamePoller {
  start(): void;
  /** Idempotent; cancels all pending timers including the auto-unfollow one-shot. */
  stop(): void;
}

export interface GamePollerOptions {
  engine: RelayEngine;
  initialPhase: GamePhase;
  startTimeUtc: string | undefined;
  /** Already clamped by the caller (CONTRACT §4). */
  liveSeconds: number;
  timers: SchedulerTimers;
  semaphore: Semaphore;
  callbacks: GamePollerCallbacks;
  /** True while the game's provider is auth-blocked (keyed providers only). */
  isProviderBlocked?: () => boolean;
}

export interface FormatOptions {
  locale: RelayLocale;
  showEmoji: boolean;
  /** True when more than one game is followed — adds the [AWY-HOM] tag. */
  multiGame: boolean;
  now(): number;
}

