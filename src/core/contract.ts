export type ProviderErrorKind =
  | 'network' // DNS/timeout/abort/5xx — retryable with backoff
  | 'rate-limit' // 429 — retry after retryAfterMs (or ≥ 60s)
  | 'auth' // 401/403 — stop polling provider until credentials change
  | 'parse' // JSON invalid or shape assumptions broken — retryable, log payload head
  | 'not-found' // 404 — game/league vanished
  | 'unavailable'; // other non-retryable (4xx misc, oversize body)

export class ProviderError extends Error {
  constructor(
    public readonly kind: ProviderErrorKind,
    message: string,
    public readonly retryAfterMs?: number,
    /** First ≤ 300 chars of the offending payload, for diagnostics. */
    public readonly payloadHead?: string,
  ) {
    super(message);
    this.name = 'ProviderError';
  }
}

/** Everything a provider may touch of the outside world. No other IO allowed. */
export interface ProviderContext {
  /** Resolved UI locale — LoL Esports uses it for the `hl` param. */
  readonly locale: RelayLocale;
  /**
   * Live value of the gameState setting. A provider whose state costs an EXTRA
   * request (LoL Esports' livestats window) MUST skip that request when false.
   * State that is free from the play payload (MLB, ESPN soccer) may ignore it —
   * the UI won't render it. Read fresh each call (getter), so a settings change
   * takes effect on the next poll without a reload.
   */
  readonly gameStateEnabled: boolean;
  /**
   * Live value of the detail setting (CONTRACT §12). 'summary' reproduces the
   * v0.2 event lists exactly; 'detailed' adds provider-composed, LOCALIZED lines
   * (MLB pitch-by-pitch, NHL shots/hits). API prose is never translated (§12.1).
   * Read fresh each call (getter) so a change takes effect on the next poll.
   */
  readonly detail: DetailLevel;
  /**
   * GET url, parse JSON. Throws ProviderError only (never raw errors).
   * Timeout 10s, body cap 8 MiB, UA 'vibe-stealth-vscode/<version>'.
   */
  fetchJson(url: string, headers?: Record<string, string>): Promise<unknown>;
  /** Secret lookup (e.g. 'pandascore.token'); undefined when unset. */
  getSecret(key: string): Promise<string | undefined>;
  /** Diagnostics sink (never user-facing relay lines). */
  log(message: string): void;
  /** Milliseconds since epoch — injectable for tests. */
  now(): number;
}

export interface PlaySnapshot {
  /** Fresh game state (score/phase/status may have advanced). */
  game: Game;
  /**
   * The FULL list of currently-known events for the game, ordered by sequence
   * ascending. RelayEngine does the diffing; providers never diff.
   */
  events: PlayEvent[];
  /**
   * Live "current state" SNAPSHOT (bases/count, lineups, draft) — the whole
   * value is replaced each poll and rendered ONLY in the tree, NEVER as relay
   * events (it is mutable; the relay is append-only). undefined when the
   * provider has no state for this game (wrong sport, pre-game, or feature off).
   * See CONTRACT §11.
   */
  state?: GameState;
}

