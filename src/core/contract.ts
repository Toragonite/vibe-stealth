export type SportKind =
  | 'baseball'
  | 'basketball'
  | 'football'
  | 'hockey'
  | 'soccer'
  | 'esports'
  | 'other';

/** pre = not started; in = live; post = finished; unknown = unparsable status. */
export type GamePhase = 'pre' | 'in' | 'post' | 'unknown';

/**
 * Absolute https URLs to a logo image (CONTRACT §13). Providers supply URLs only;
 * they never fetch images. The UI's logoCache downloads, validates and caches them.
 */
export interface LogoRef {
  light: string;
  /** Dark-theme variant when the source offers one; else the UI reuses `light`. */
  dark?: string;
}

export interface League {
  /** Provider-scoped stable id, e.g. 'nba', 'eng.1', 'mlb', 'nhl', 'lol'. */
  id: string;
  providerId: string;
  /** English display name; UI may localize known ids. */
  name: string;
  sport: SportKind;
  logo?: LogoRef;
}

export interface TeamSide {
  /** Provider-native team id; '' if unknown. */
  id: string;
  /**
   * Best display name the provider offers (NHL score/now only carries the
   * nickname, e.g. 'Stars'). Never empty — fall back to abbrev or 'TBD'.
   */
  name: string;
  /** Short code ≤ 5 chars for status bar / line tags, e.g. 'STL'. Derived from name if absent. */
  abbrev: string;
  /**
   * Integer ≥ 0, or undefined when unknown/unparsable. Providers must coerce
   * string scores; coercion failure ⇒ undefined, never NaN or negative.
   */
  score: number | undefined;
  /** CONTRACT §13. Omitted when the provider has no logo for this team. */
  logo?: LogoRef;
}

export interface Game {
  /** Globally unique: `${providerId}:${leagueId}:${nativeGameId}`. */
  id: string;
  providerId: string;
  leagueId: string;
  leagueName: string;
  sport: SportKind;
  /** ISO-8601 UTC with seconds ('2026-07-08T19:45:00Z'), or undefined if unparsable. */
  startTimeUtc: string | undefined;
  phase: GamePhase;
  /** Human status, locale-neutral, e.g. 'Top 7th', 'Q3 04:12', "67'", 'Final'. Never empty. */
  statusText: string;
  /** ≤ 8 chars for the status bar, e.g. 'T7', 'Q3', "67'", 'FT', 'HT', 'G2'. Never empty. */
  statusShort: string;
  home: TeamSide;
  away: TeamSide;
}

export type PlayEventKind =
  | 'play' // ordinary play-by-play line
  | 'score' // a play that changed the score
  | 'status' // period/inning/game boundary ('End of the 9th', 'Game Final')
  | 'correction' // re-emission of an already-emitted event whose text changed
  | 'system'; // lines generated by the relay itself (backfill notice, errors…)

export interface PlayEvent {
  /**
   * Stable within a game. Providers use native ids when stable; otherwise
   * derive as documented in docs/CONTRACT.md §Providers. Never empty.
   */
  id: string;
  gameId: string;
  /**
   * Monotonic ordering key within the game's full event list (array index or
   * native sort key per docs/CONTRACT.md). Ties broken by id (lexicographic).
   */
  sequence: number;
  /** Display clock within period, e.g. '04:12', or undefined (baseball). */
  clock: string | undefined;
  /** Display period label, e.g. 'Q3', 'P2', '7회말', 'T7', or undefined. */
  period: string | undefined;
  /** Single line of prose. Non-empty, no control chars, no newlines, ≤ 500 chars. */
  text: string;
  kind: PlayEventKind;
  /** Present when the provider knows the score as of this event. */
  scoreAfter: { home: number; away: number } | undefined;
}

